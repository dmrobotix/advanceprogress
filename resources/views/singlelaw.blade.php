        <h1>Preamble.</h1>
        <p>WHEREAS, The legislature finds that disenfranchisement of people convicted of a criminal offense undermines democratic ideals by depriving otherwise-qualified citizens of their right to vote; </p>
        <p>WHEREAS, Nationally, disenfranchisement based on criminal conviction has a disproportionate effect on communities of color, which experience a higher incarceration rate than white communities; </p>
          <p>  WHEREAS, in [Insert State Name Here], this loss of voting power is a burden borne mostly by members of the Native American community, the Latino community and communities of Color, who are and have historically been disproportionately incarcerated;</p>
        <p>WHEREAS, the loss of the right to vote impedes the successful reentry of convicted and incarcerated people as responsible, productive citizens into the community; </p>
        <p> WHEREAS, voting can be an opportunity for incarcerated people to learn about candidates and issues, engage in civic education, and prepare for reintegration into society; </p>
        <p>     WHEREAS, disenfranchisement can suppress electoral participation among eligible voters by discouraging voting among family and community members of disenfranchised persons;</p>
        <p>WHEREAS, restoration of voting rights to those convicted of a criminal offense is often granted in a non-uniform and potentially discriminatory manner; </p>
              <p>   WHEREAS, basic constitutional principles of fairness and equal protection require an equal opportunity for people to vote;</p>
       <p> WHEREAS, allowing incarcerated people to vote by absentee ballot will benefit the State by helping incarcerated people remain aware of issues that are important to society so that they may participate more fully in their communities upon release;</p>

